inline void put_px(int x, int y, uint16_t c) {
    if (x < 0 || y < 0 || static_cast<unsigned>(x) >= s_w || static_cast<unsigned>(y) >= s_h) {
        return;
    }
    s_fb[static_cast<size_t>(y) * s_w + static_cast<size_t>(x)] = c;
}

void clear_fb(uint16_t c) {
    const size_t n = static_cast<size_t>(s_w) * s_h;
    for (size_t i = 0; i < n; ++i) {
        s_fb[i] = c;
    }
}

void draw_char(int x, int y, char ch, int scale, uint16_t c) {
    if (ch < 32 || ch > 127) {
        ch = '?';
    }
    const uint8_t *g = kFont5x7[static_cast<int>(ch) - 32];
    for (int col = 0; col < 5; ++col) {
        const uint8_t bits = g[col];
        for (int row = 0; row < 7; ++row) {
            if (bits & (1U << row)) {
                for (int sx = 0; sx < scale; ++sx) {
                    for (int sy = 0; sy < scale; ++sy) {
                        put_px(x + col * scale + sx, y + row * scale + sy, c);
                    }
                }
            }
        }
    }
}

void draw_text(int x, int y, const char *txt, int scale, uint16_t c) {
    int cx = x;
    for (const char *p = txt; p && *p; ++p) {
        draw_char(cx, y, *p, scale, c);
        cx += (5 * scale) + scale;
    }
}

void format_hms(uint64_t uptime_s, char *buf, size_t buf_sz) {
    const uint64_t hh = uptime_s / 3600ULL;
    const uint64_t mm = (uptime_s % 3600ULL) / 60ULL;
    const uint64_t ss = uptime_s % 60ULL;
    std::snprintf(buf, buf_sz, "%02llu:%02llu:%02llu",
                  static_cast<unsigned long long>(hh),
                  static_cast<unsigned long long>(mm),
                  static_cast<unsigned long long>(ss));
}

void render_screen(const ui_model::UiStatusModel &m) {
    clear_fb(COLOR_BG);

    char line[64];
    char uptime[24];
    format_hms(m.uptime_s, uptime, sizeof uptime);

    int y = 2;
    const int lh = 10;

    std::snprintf(line, sizeof line, "Crypto-Alert V3");
    draw_text(2, y, line, 1, COLOR_FG); y += 10;
    std::snprintf(line, sizeof line, "Ver: %s", m.version);
    draw_text(2, y, line, 1, COLOR_FG); y += lh;

    std::snprintf(line, sizeof line, "Price: %.1f", m.last_price);
    draw_text(2, y, line, 1, COLOR_FG); y += lh;

    std::snprintf(line, sizeof line, "WiFi:%s WS:%s", m.wifi_ready ? "OK" : m.wifi_state, m.ws_live ? "LIVE" : m.ws_state);
    draw_text(2, y, line, 1, COLOR_FG); y += lh;

    std::snprintf(line, sizeof line, "Up:%s RX:%llums", uptime, static_cast<unsigned long long>(m.ws_last_rx_age_ms));
    draw_text(2, y, line, 1, COLOR_FG); y += lh;

    std::snprintf(line, sizeof line, "r1:%s%.2f%%", m.ret_1m_valid ? "" : "NA ", m.ret_1m_valid ? m.ret_1m_pct : 0.0);
    draw_text(2, y, line, 1, COLOR_FG); y += lh;
    std::snprintf(line, sizeof line, "r5:%s%.2f%%", m.ret_5m_valid ? "" : "NA ", m.ret_5m_valid ? m.ret_5m_pct : 0.0);
    draw_text(2, y, line, 1, COLOR_FG); y += lh;
    std::snprintf(line, sizeof line, "r30:%s%.2f%%", m.ret_30m_valid ? "" : "NA ", m.ret_30m_valid ? m.ret_30m_pct : 0.0);
    draw_text(2, y, line, 1, COLOR_FG); y += lh;

    std::snprintf(line, sizeof line, "Reg:%s", m.regime);
    draw_text(2, y, line, 1, COLOR_FG); y += lh;

    std::snprintf(line, sizeof line, "S1:%s%.1f", m.support_1_valid ? "" : "NA ", m.support_1_valid ? m.support_1 : 0.0);
    draw_text(2, y, line, 1, COLOR_FG); y += lh;
    std::snprintf(line, sizeof line, "R1:%s%.1f", m.resistance_1_valid ? "" : "NA ", m.resistance_1_valid ? m.resistance_1 : 0.0);
    draw_text(2, y, line, 1, COLOR_FG); y += lh;

    std::snprintf(line, sizeof line, "Setup:%s", m.setup_state);
    draw_text(2, y, line, 1, COLOR_FG); y += lh;
    std::snprintf(line, sizeof line, "Trg:%s", m.trigger_state);
    draw_text(2, y, line, 1, COLOR_FG); y += lh;
    std::snprintf(line, sizeof line, "Alert:%s", m.alert_state);
    draw_text(2, y, line, 1, COLOR_FG); y += lh;

    std::snprintf(line, sizeof line, "Heap:%lu Min:%lu", static_cast<unsigned long>(m.free_heap),
                  static_cast<unsigned long>(m.min_free_heap));
    draw_text(2, y, line, 1, COLOR_FG);

    (void)esp_lcd_panel_draw_bitmap(board_support::display_panel(), 0, 0, static_cast<int>(s_w), static_cast<int>(s_h), s_fb);
}

void ui_task(void *arg) {
    (void)arg;
    bool first = true;
    while (true) {
        ui_model::UiStatusModel m{};
        if (ui_model::get_status_model(&m) && board_support::display_ready()) {
            render_screen(m);
            if (first) {
                first = false;
                ESP_LOGI(TAG, "first render ok");
            }
        }
        vTaskDelay(pdMS_TO_TICKS(kRefreshMs));
    }
}

